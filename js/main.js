function ver_noticia(){
    $('#box_noticia').show();
    $('#box_publicidad').hide();
    $('#box_imagen').hide();
}
function ver_publicidad(){
	$('#box_noticia').hide();
    $('#box_publicidad').show();
    $('#box_imagen').hide();
}
function ver_imagen(){
	$('#box_noticia').hide();
    $('#box_publicidad').hide();
    $('#box_imagen').show();
}


function ver_regional(){
	$('#box_regional').show();
    $('#box_provincial').hide();
    $('#box_distrital').hide();
}
function ver_provincial(){
	$('#box_regional').hide();
    $('#box_provincial').show();
    $('#box_distrital').hide();
}
function ver_distrital(){
	$('#box_regional').hide();
    $('#box_provincial').hide();
    $('#box_distrital').show();
}


